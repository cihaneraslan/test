package com.cgiser.moka.result;

public class EmblemResult {
	private int cash;
	private Long contribute1;
	private Long honor;
	private Long myContribute;
	public int getCash() {
		return cash;
	}
	public void setCash(int cash) {
		this.cash = cash;
	}
	public Long getContribute1() {
		return contribute1;
	}
	public void setContribute1(Long contribute1) {
		this.contribute1 = contribute1;
	}
	public Long getHonor() {
		return honor;
	}
	public void setHonor(Long honor) {
		this.honor = honor;
	}
	public Long getMyContribute() {
		return myContribute;
	}
	public void setMyContribute(Long myContribute) {
		this.myContribute = myContribute;
	}
}
