package com.cgiser.moka.model;

public class CardFragment {
	private int id;
	private int cardId;
	private String cardName;
	private int num;
	private int type;
	private int resolveNum;
	private int resolveType;
	private int universal;
	private int state;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getCardId() {
		return cardId;
	}
	public void setCardId(int cardId) {
		this.cardId = cardId;
	}
	public String getCardName() {
		return cardName;
	}
	public void setCardName(String cardName) {
		this.cardName = cardName;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	public int getResolveNum() {
		return resolveNum;
	}
	public void setResolveNum(int resolveNum) {
		this.resolveNum = resolveNum;
	}
	public int getUniversal() {
		return universal;
	}
	public void setUniversal(int universal) {
		this.universal = universal;
	}
	public int getResolveType() {
		return resolveType;
	}
	public void setResolveType(int resolveType) {
		this.resolveType = resolveType;
	}

}
